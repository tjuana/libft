#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t		i;
	char		*s;

	i = 0;
	if (!(s = ft_memalloc(size + 1)))
		return (NULL);
	while (i < size)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
